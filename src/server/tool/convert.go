package tool

import "regexp"

func String(n int32) string {
	buf := [11]byte{}
	pos := len(buf)
	i := int64(n)
	signed := i < 0
	if signed {
		i = -i
	}
	for {
		pos--
		buf[pos], i = '0'+byte(i%10), i/10
		if i == 0 {
			if signed {
				pos--
				buf[pos] = '-'
			}
			return string(buf[pos:])
		}
	}
}

func IsNum(str string) bool {
	pattern := "\\d+" //反斜杠要转义
	result, _ := regexp.MatchString(pattern, str)
	return result
}
